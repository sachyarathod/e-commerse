package loopStudy;

public class StudyOfLoop {

	public static void main(String[] args) 
	{
		
		
		
		
		
		// i wany to print 1 to 50 values in desending order
		
		
		int i =50;
		
	   while(i>=1) 
	   {
		   System.out.println(i);
		   i--;
	   }
		
	   int j=50;
	   
	   // do while loop
	   do 
	   {
		    System.out.println(j);
		    j--;
	   }while(j>=1);
		
		
		
		
		
		
		
		

	}

}
