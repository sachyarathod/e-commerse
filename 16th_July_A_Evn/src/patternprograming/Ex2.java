package patternprograming;

public class Ex2 {

	public static void main(String[] args) 
	{
		
		// *****
		// *****
		// *****
		// *****
		
		// outer for loop for rows
		for(int i=1;i<=4;i++)
		{
			// inner for loop for column
			for(int j =1;j<=4;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
