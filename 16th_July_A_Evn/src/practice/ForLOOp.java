package practice;

public class ForLOOp {

	public static void main(String[] args)
	{
		
		// i want to print 4 table using for loop
		
		for(int i=4;i<=40;i=i+4)
		{
			System.out.println(i);
		}
		
		// i want to print 10 table using while loop
		
		int i=100;
		while(i>=10) 
		{
		 System.out.println(i);
		 i=i-10;
		}
		
		// i want to print 20 table using do while loop
		
		int j =20;
		
		do
		{
			System.out.println(j);
			j=j+20;
			
		}
		while(j<=200);

	}

	

}
