package loopsStudy;

public class WhileLoop {

	public static void main(String[] args) 
	{
		// i want to write 9 table
		
		int i=9;
		while(i<=90)
		{
			System.out.println(i);
			i=i+9;
		}
		
		// i want to write 9 table in reverse order
		
		int j=90;
		while(j>=9)
		{
			System.out.println(j);
			j=j-9;
		}
		
		int p=12;
		while(p<=120)
		{
			System.out.println(p);
			p=p+12;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
